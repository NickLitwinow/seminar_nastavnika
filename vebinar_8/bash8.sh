#!/bin/bash

# Получаем процент использования диска
USAGE=$(df / | grep / | awk '{ print $5 }' | sed 's/%//g')

# Устанавливаем порог
THRESHOLD=80

# Проверяем, превышает ли использование порог
if [ "$USAGE" -gt "$THRESHOLD" ]; then
    echo "Внимание: Использование диска составляет ${USAGE}%, что превышает порог в ${THRESHOLD}%."
else
    echo "Использование диска составляет ${USAGE}%, что в пределах нормы."
fi